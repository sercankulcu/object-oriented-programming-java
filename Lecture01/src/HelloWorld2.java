/*
 * Here is a simple Java program that uses a method to print "Hello, World!" 
 * to the console:
 * 
 * Note that the printHelloWorld method is defined with the static keyword, 
 * which means it can be called directly from the main method without having 
 * to create an instance of the HelloWorld2 class.
 * 
 * */

public class HelloWorld2 {
	
	public static void main(String[] args) {
		
		printHelloWorld();
	}

	public static void printHelloWorld() {
		
		System.out.println("Hello, World!");
	}
}
